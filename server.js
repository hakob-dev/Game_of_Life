setInterval(serverDraw, 3000);
function serverDraw() {
    for (var i in grassArr) {
        grassArr[i].bazmanal();
    }
    for (var i in xotakerArr) {
        xotakerArr[i].eat();
        xotakerArr[i].evolve();
        xotakerArr[i].hunt();
    }
    for (var i in gishatichArr) {
        gishatichArr[i].eat();
        gishatichArr[i].evolve();
        gishatichArr[i].die()
    }
    for (var i in popoxakanArr) {
        popoxakanArr[i].eat()
    }
    for (var i in popoxichArr) {
        popoxichArr[i].eat()
    }

    io.sockets.emit('matrix', matrix)
}

















